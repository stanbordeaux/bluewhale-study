<h1>Donations</h1>
<p>The Blue Whale Study Inc. is a small, independent research and education institute. </p>
<p>While we receive some government funding, it is insufficient to meet our needs. Funding is required for ongoing field research programs, education programs, administrative costs, and of course for salary. </p>
<p>The last few years have been difficult financially but that makes us only more determined to build a better future. </p>
<p>With this in mind we applied for and received Tax Deductible Gift Recipient and Charity status from the Australian Taxation Office in January 2009. </p>
<p>Donations over $2 to Blue Whale Study Inc. are tax-deductible. </p>
